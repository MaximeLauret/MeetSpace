#!/bin/bash
#Test de Master

userName=userTest-`date +%Y-%m-%d-%H-%M`
userProject=projetTest-`date +%Y-%m-%d-%H-%M`
#------------------------------------------------------------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------------------------------------------------------------

echo " add start .."

echo " 		Ajout d'un utilisateur"
#------------------------------------------------------------------------------------------------------------------------------------------------------------
./master.sh user add $userName $userName

echo " 		Ajout d'un projet"
#------------------------------------------------------------------------------------------------------------------------------------------------------------
./master.sh project add $userName

echo " 		Ajout d'un utilisateur à l'alias d'un projet"
#------------------------------------------------------------------------------------------------------------------------------------------------------------
./master.sh project alias add $userName $userProject


echo "		Activation du vhost du projet:"
#------------------------------------------------------------------------------------------------------------------------------------------------------------
./master.sh project vhost enable $userProject
echo " ..end start"
#------------------------------------------------------------------------------------------------------------------------------------------------------------
#------------------------------------------------------------------------------------------------------------------------------------------------------------



echo " del start .."


echo " 		Suppresion d'un utilisateur à l'alias d'un projet"
#------------------------------------------------------------------------------------------------------------------------------------------------------------
./master.sh project alias del $userName $userProject



echo "		Désactivation du vhost du projet:"
#------------------------------------------------------------------------------------------------------------------------------------------------------------
./master.sh project vhost disable $userProject
echo " ..end del"

echo " 		Alias: Suppresion d'un projet"
#------------------------------------------------------------------------------------------------------------------------------------------------------------
./master.sh project del $userProject



echo " 		Suppresion d'un utilisateur"
#------------------------------------------------------------------------------------------------------------------------------------------------------------
./master.sh user del $userName



